func TestParseGoMod(t *testing.T) {
	tmpDir := t.TempDir()
	goModPath := filepath.Join(tmpDir, "go.mod")

	content := `module example.com/myproject

go 1.21

require (
	github.com/gin-gonic/gin v1.9.1
	github.com/stretchr/testify v1.8.4
)

require (
	github.com/indirect/dep v0.1.0 // indirect
)
`
	if err := os.WriteFile(goModPath, []byte(content), 0644); err != nil {
		t.Fatal(err)
	}

	var packages []Package
	var scannedProjects []string
	seen := make(map[string]bool)
	addManager := func(string) {}

	parseGoMod(goModPath, &packages, &scannedProjects, seen, addManager)

	if len(packages) != 3 {
		t.Fatalf("packages length = %d, want 3", len(packages))
	}

	names := make(map[string]string)
	for _, p := range packages {
		names[p.Name] = p.Version
		if p.PackageManager != "go" {
			t.Errorf("PackageManager = %q, want %q", p.PackageManager, "go")
		}
		if p.InstallType != "project" {
			t.Errorf("InstallType = %q, want %q", p.InstallType, "project")
		}
	}
	if names["github.com/gin-gonic/gin"] != "v1.9.1" {
		t.Errorf("gin version = %q, want v1.9.1", names["github.com/gin-gonic/gin"])
	}
}

