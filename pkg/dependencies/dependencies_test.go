func TestNewDependencyResult_ComputesTotals(t *testing.T) {
	packages := []Package{
		{Name: "requests", Version: "2.28.0", PackageManager: "pip"},
		{Name: "flask", Version: "2.2.0", PackageManager: "pip"},
		{Name: "express", Version: "4.18.0", PackageManager: "npm"},
	}

	result := NewDependencyResult(packages, []string{"/project1"}, nil)

	if result.TotalPackages != 3 {
		t.Errorf("TotalPackages = %d, want 3", result.TotalPackages)
	}
	if result.Timestamp == "" {
		t.Error("Timestamp should not be empty")
	}
	if len(result.PackageManagersFound) != 2 {
		t.Errorf("PackageManagersFound length = %d, want 2", len(result.PackageManagersFound))
	}
	if result.Summary["pip"] != 2 {
		t.Errorf("Summary[pip] = %d, want 2", result.Summary["pip"])
	}
	if result.Summary["npm"] != 1 {
		t.Errorf("Summary[npm] = %d, want 1", result.Summary["npm"])
	}
}

func TestNewDependencyResult_GroupsByManager(t *testing.T) {
	packages := []Package{
		{Name: "a", Version: "1.0", PackageManager: "pip"},
		{Name: "b", Version: "2.0", PackageManager: "npm"},
		{Name: "c", Version: "3.0", PackageManager: "pip"},
	}

	result := NewDependencyResult(packages, nil, nil)

	if len(result.PackagesByManager["pip"]) != 2 {
		t.Errorf("PackagesByManager[pip] length = %d, want 2", len(result.PackagesByManager["pip"]))
	}
	if len(result.PackagesByManager["npm"]) != 1 {
		t.Errorf("PackagesByManager[npm] length = %d, want 1", len(result.PackagesByManager["npm"]))
	}
}

